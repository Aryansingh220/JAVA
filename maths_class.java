public class maths_class {
    public static void main(String[] args) {
        System.out.println(Math.abs(-678));         // Absolute value
        System.out.println(Math.ceil(76.45));       // Rounds up
        System.out.println(Math.floor(76.45));      // Rounds down
        System.out.println(Math.pow(4, 2));         // 4^2
        System.out.println(Math.sqrt(16));          // Square root
        System.out.println(Math.random());          // Random number between 0.0 and 1.0
    }
}

                               

















































